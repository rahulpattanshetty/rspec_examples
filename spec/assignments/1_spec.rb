RSpec.describe "Math Calculations" do
  
  it "does addition operation" do
    expect(4+4).to eq(8)
  end

  it "does minus operation" do
    expect(4-4).to eq(0)
  end
  
  it "does multiplication operation" do
    expect(4*4).to eq(16)
  end

  it "does division operation" do
    expect(4/4).to eq(1)
  end

end